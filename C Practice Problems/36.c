#include <stdio.h>
int main()
{
  int i = -3;
  int k = i % 2;
  printf("%d\n", k);
}

/* In this code, i is initialized with the value -3 and then the expression i % 2 is evaluated. The modulus operator returns the remainder of dividing the left operand by the right operand. In this case, the remainder of dividing -3 by 2 is -1, so that is the value that is assigned to the variable k and then printed. */